#define BUF_OFFSET     1440		/* each plane has a buffer size of 1440.*/
#define BUF_SIZE   320 * 18		/* buffer needs size of 320 * 18 for 4 planes in total.*/
#define LINESIZE   		 80		/* each plane needs 80 numbers for per line.*/
#define LINESIZEPLUS   	 81		/* since per charcter occupies two numbers per plane in one line, it needs extra 1.*/
#define LINESIZEPPLUS    82		/* offset set for plane 3.*/
#define FOUR   			  4		/* total nuber of planes.*/
#define ZERO			  0		/* constant 0.*/
#define ONE				  1		/* constant 1.*/
#define TWO				  2		/* constant 2.*/
#define THREE			  3		/* constant 3.*/
#define FIVE			  5		/* constant 5.*/
#define SIX				  6		/* constant 6.*/
#define SEVEN			  7		/* constant 7.*/
#define BITMASKNUMBER  0x01		/* constant 0x01 used to bitmask the input elements.*/
#define CHARSIZE	   	 16		/* a character has 16 lines to be printed.*/
#define YESCHARCOLOR   0x25		/* new color if tis pixels is covered by characters.*/
#define NOCHARCOLOR	   0x09		/* new color if tis pixels is not covered by characters.*/
#define ARRAY_SIZE		 40		/* array size is 40.*/

/*
 * texttoimage
 *     DESCRIPTION: Copy one plane of a screen from the build buffer to the video memory.
 *     INPUTS: str -- a pointer to a unsigned char array which we need to print them to the screen.
 *     OUTPUTS: none
 *     RETURN VALUE: buf -- a pointer to the unsigned char buffer where we just build in this function.
 *     SIDE EFFECTS: copies a plane from the build buffer to video memory
 */
unsigned char* texttoimage(unsigned char *str)
{
		/* using malloc to create a buffer of fixed size 320 * 18,
		 * this is what we want to filled with.*/
		unsigned char *buf;
		buf = (unsigned char *)malloc(BUF_SIZE);
		int counter = ZERO;
		int planenumber = ZERO;
		int linenumber = ZERO;
		int i = ZERO;
		int offset = ZERO;
		
		/* using the double while loop to go through the char array and build the buffer.
		 * First we filled the center 16 lines.*/
		while (linenumber < CHARSIZE) /* check if we have completed 16 lines.*/
		{
			while (counter < ARRAY_SIZE) /* check if we go through all the array.*/
			{
				while (planenumber < FOUR) /* check if we go through all 4 planes.*/
				{
					if (planenumber == ZERO)
					{
						/* since buffer should be built in a reverse order, we need to calculate offset.
						 * for plane 0, its offset is 3 * 1440.*/
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZE ] = (font_data[str[counter]][linenumber] >> SEVEN) & BITMASKNUMBER;
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZEPLUS] = (font_data[str[counter]][linenumber] >> THREE) & BITMASKNUMBER;
					}
					else if (planenumber == ONE)
					{
						/* since buffer should be built in a reverse order, we need to calculate offset.
						 * for plane 1, its offset is 2 * 1440.*/
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZE ] = (font_data[str[counter]][linenumber] >> SIX) & BITMASKNUMBER;
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZEPLUS] = (font_data[str[counter]][linenumber] >> TWO) & BITMASKNUMBER;
					}
					else if (planenumber == TWO)
					{
						/* since buffer should be built in a reverse order, we need to calculate offset.
						 * for plane 2, its offset is 1 * 1440.*/
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZE ] = (font_data[str[counter]][linenumber] >> FIVE) & BITMASKNUMBER;
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZEPLUS] = (font_data[str[counter]][linenumber] >> ONE) & BITMASKNUMBER;
					}
					else if (planenumber == THREE)
					{
						/* since buffer should be built in a reverse order, we need to calculate offset.
						 * for plane 3, its offset is 0 * 1440.*/
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZEPLUS] = (font_data[str[counter]][linenumber] >> FOUR) & BITMASKNUMBER;
						buf[(THREE - planenumber) * BUF_OFFSET + offset + LINESIZEPPLUS] = (font_data[str[counter]][linenumber] >> ZERO) & BITMASKNUMBER;
					}
					planenumber++;
				}
				
				/*offset plus two since one character occupies two seats per line.*/
				offset = offset + TWO; 
				planenumber = ZERO;
				counter++;
			}
			linenumber++;
			planenumber = ZERO;
			counter = ZERO;
			
			/* recalculate the offset for next loop.*/
			offset = linenumber * LINESIZE;
		}
		counter = ZERO;
		i = ZERO;
		
		/* in this loop, we fill the padding two lines with zero by double while loop.*/
		while (counter < FOUR)
		{
			while (i < LINESIZE )
			{
				buf[counter * BUF_OFFSET + i] = ZERO;
				i++;
			}
			counter++;
		}
		i = ZERO;
		
		/* In order to make status bar easy to read, we need to reset colors for elements.*/
		while (i < BUF_SIZE)
		{
			if (buf[i] == ONE)
				buf[i] = YESCHARCOLOR; /*set 1 to new color.*/
			else
				buf[i] = NOCHARCOLOR; /*set 0 to new color.*/
			i = i + ONE;
		}
		return buf;
		
}

